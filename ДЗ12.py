#Задание 6 Напишите программу которая позволяет переводить время вводимое пользователем с клавиатуры и представленное в секундах в вермя представленное в часах, минутах и секундах.

time_seconds = int(input("Enter seconds: "))
hours = int(time_seconds / 60 / 60)
time_hours = time_seconds - (hours * 60 *60)
minutes = int(time_hours / 60)
seconds = time_hours - (minutes * 60)
