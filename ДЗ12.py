#Задание 5 Напишите программу: Вася обычно спит ночью X часов и устраивает себе днем тихий час на Y минут. Определите, сколько всего минут Вася спит в сутки. Внимание, программа принимает значения X и Y из стандартного потока ввода (функция input), результат надо выводить в стандартный поток вывода (функция print). Обратите внимание на то, что приглашение, переданное в качестве аргумента в функцию input, считается выводом вашей программы.

